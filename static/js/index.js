function navBarFunction() {
  document.getElementById('menu-mbl').style.display='flex';
  document.getElementById('nav-close').style.display='flex';    
  document.getElementById('mbl-menu-bar').style.display='none';    
}
function navBarCloseFunction() {
  document.getElementById('menu-mbl').style.display='none';
  document.getElementById('nav-close').style.display='none';    
  document.getElementById('mbl-menu-bar').style.display='contents';    
}